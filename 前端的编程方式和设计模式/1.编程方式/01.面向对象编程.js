
/**
 * 面向对象编程其实是对面向过程编程的一种封装
 * 
 *   例如：洗衣服
 *    
 *   1.打开冰箱
 *   2.到入水
 *   3.放入衣服
 *   4.关闭洗衣机门
 *   5.点击洗衣按钮
 * 
 *  将以上的步奏进行封装，然后实例成对象进行调用
 * 
 *  面向对象的基本条件就是  实例化 封装 继承 多态
 */


// 面向对象的好处：
// • 封装特性能够提高类的易用性，减少编程过程中代码出错的风险。
// • 继承最大的好处就是能够实现代码的复用。
// • 抽象更多的是能够让程序的设计和实现分离。
// • 多态最大的好处就是提高了程序的可扩展性。

// 面向对象的不足：
// • 万物皆对象，大量的创建对象会占用大量的内存空间。
// • 需要有一定的软件支持环境。