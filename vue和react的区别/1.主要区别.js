/**
 * 
 *     vue 和 react 的区别
 *     1.vue 是一个渐进式的框架，而react是一个用于构建用户界面的javaScript的库。
 *     2.vue提供了组件编写较为固定的模式，而react是结合jsx语法进行组件的编写。
 *     3.vue的响应式可以做到对属性改变的细颗粒的监听，而react需要手动的采用shoudComponent 和memo 去比较。
 *     4.vue有提供组件编写样式的区域，而react只是解决了ui层，组件的样式还是要借助第三方库。
 *     5.事件机制不同，vue采用原生的web事件，而react采用合成事件。
 *     6.vue提供了指令供开发者使用，而react则是更加依赖原生js的用法。
 *   
*/